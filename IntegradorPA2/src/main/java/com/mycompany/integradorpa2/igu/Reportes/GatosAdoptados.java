    private void configurarTabla() {
    modelo = new DefaultTableModel(
            new Object[]{"ID Gato", "Nombre", "Tipo adopción", "Estado", "Familia", "Fecha adopción"}, 0
    ) {
        @Override public boolean isCellEditable(int r, int c) { return false; }
        @Override public Class<?> getColumnClass(int c) {
            return (c == 0) ? Long.class : String.class;
        }
    };
    tablaGatos.setModel(modelo);
    tablaGatos.getTableHeader().setReorderingAllowed(false);
}

    private void cargarTabla() {
    modelo.setRowCount(0);

    List<Adopcion> adopciones = adopcionDao.listarTodos();
    System.out.println("Adopciones en BD: " + adopciones.size());

    for (Adopcion a : adopciones) {
        Gato g = a.getGato();
        if (g == null) continue;

        TipoAdopcion tipo = a.getTipoAdopcion();
        Familia f = a.getFamilia();
        String estado = (a.getEstado() != null) ? a.getEstado().name() : "";

        modelo.addRow(new Object[]{
                g.getId(),
                nv(g.getNombre()),
                (tipo != null ? tipo.name() : ""),
                estado,
                (f != null ? nv(f.getNombre()) : ""),
                (a.getFechaAdopcion() != null ? a.getFechaAdopcion().toString() : "")
        });
    }
}

private String nv(String s) {
    return (s == null) ? "" : s;
}

private void configurarFiltro() {
    sorter = new TableRowSorter<>(modelo);
    tablaGatos.setRowSorter(sorter);

    DocumentListener dl = new DocumentListener() {
        public void insertUpdate(javax.swing.event.DocumentEvent e) { filtrar(); }
        public void removeUpdate(javax.swing.event.DocumentEvent e) { filtrar(); }
        public void changedUpdate(javax.swing.event.DocumentEvent e) { filtrar(); }
    };
    txtBuscar.getDocument().addDocumentListener(dl);
}

private void filtrar() {
    String txt = txtBuscar.getText().trim();
    if (txt.isEmpty()) {
        sorter.setRowFilter(null);
    } else {
        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + txt));
    }
}

    
    


