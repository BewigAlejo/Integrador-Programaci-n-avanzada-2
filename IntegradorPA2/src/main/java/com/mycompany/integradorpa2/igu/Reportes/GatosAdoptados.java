    private void botonReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonReporteActionPerformed
        int temp = 0;
        int defi = 0;

    for (int i = 0; i < modelo.getRowCount(); i++) {
        Object val = modelo.getValueAt(i, 2); // "Tipo adopción"
        if (val == null) continue;
        String tipo = val.toString().toUpperCase();

        if (tipo.contains("TEMP")) {
            temp++;
        } else if (tipo.contains("DEF")) {
            defi++;
        }
    }

    JOptionPane.showMessageDialog(
            this,
            "Hay " + temp + " gato(s) adoptados TEMPORALMENTE\n"
          + "y " + defi + " gato(s) adoptados DEFINITIVAMENTE.",
            "Reporte de Adopciones",
            JOptionPane.INFORMATION_MESSAGE
    );
    }//GEN-LAST:event_botonReporteActionPerformed

