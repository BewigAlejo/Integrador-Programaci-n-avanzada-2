    private void botonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCrearActionPerformed
        // TODO add your handling code here:
        try {
            // completar datos específicos
            vet.setEspecialidad(txtEspecialidad.getText().trim());
            vet.setMatricula(txtMatricula.getText().trim());

            // validaciones simples
            if (vet.getEspecialidad() == null || vet.getEspecialidad().isBlank()
                || vet.getMatricula() == null || vet.getMatricula().isBlank()) {
                JOptionPane.showMessageDialog(this, "Completá especialidad y matrícula.");
                return;
            }

            dao.crear(vet);  // ← recién acá se PERSISTE
            JOptionPane.showMessageDialog(this, "Veterinario creado. ID = " + vet.getId());
            if (onClose != null) onClose.run();
            dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se pudo guardar: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_botonCrearActionPerformed

