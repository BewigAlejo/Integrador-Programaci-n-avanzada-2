    private void cargar() {
        try {
            Optional<Familia> opt = dao.buscarPorId(id);
            if (opt.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No se encontró la familia id=" + id, "Aviso",
                        JOptionPane.WARNING_MESSAGE);
                dispose();
                return;
            }
            familia = opt.get();

            // --- base (heredados de Usuario)
            txtNombre.setText(nvl(familia.getNombre()));
            txtEmail.setText(nvl(familia.getEmail()));
            txtTelefono.setText(nvl(familia.getTelefono()));
            txtUsuario.setText(nvl(familia.getUsuario()));
            txtContrasenia.setText(nvl(familia.getContrasenia()));

            // --- específicos
            txtDireccion.setText(nvl(familia.getDireccion()));
            txtCoordenadas.setText(nvl(familia.getCoordenadas()));
            Integer rep = familia.getReputacion();
            spinnerReputacion.setValue(rep != null ? rep : 0);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error cargando familia: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
            dispose();
        }
    }
    
   

