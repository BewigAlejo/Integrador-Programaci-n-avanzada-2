    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
            try {
                // --- base
                familia.setNombre(txtNombre.getText().trim());
                familia.setEmail(txtEmail.getText().trim());
                familia.setTelefono(txtTelefono.getText().trim());
                familia.setUsuario(txtUsuario.getText().trim());
                familia.setContrasenia(new String(txtContrasenia.getPassword()));

                // --- específicos
                familia.setDireccion(txtDireccion.getText().trim());
                familia.setCoordenadas(txtCoordenadas.getText().trim());
                familia.setReputacion((Integer) spinnerReputacion.getValue());

                dao.actualizar(familia);

                JOptionPane.showMessageDialog(this, "Familia actualizada correctamente.");
                dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "No se pudo actualizar: " + ex.getMessage(),
                        "Error", JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
    }//GEN-LAST:event_botonActualizarActionPerformed

