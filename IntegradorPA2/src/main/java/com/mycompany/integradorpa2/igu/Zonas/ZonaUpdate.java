    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        try {
            Optional<Zona> opt = zonaDao.buscarPorId(id);
            if (opt.isEmpty()) {
                JOptionPane.showMessageDialog(this, "La zona ya no existe.");
                dispose();
                return;
            }
            Zona z = opt.get();

            String nombre = txtNombre.getText().trim();
            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El nombre es obligatorio.");
                txtNombre.requestFocus();
                return;
            }
            Double lat = parseDoubleOrNull(txtCoorLat.getText().trim());
            Double lon = parseDoubleOrNull(txtCoorLon.getText().trim());

            z.setNombreZona(nombre);
            z.setCoor_lat(lat);
            z.setCoor_lon(lon);

            zonaDao.actualizar(z);
            JOptionPane.showMessageDialog(this, "Zona actualizada.");
            dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se pudo actualizar: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_botonActualizarActionPerformed

