    private void cargar() {
        try {
            Optional<Zona> opt = zonaDao.buscarPorId(id);
            if (opt.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No se encontr√≥ la zona ID = " + id);
                dispose();
                return;
            }
            Zona z = opt.get();
            txtNombre.setText(nvl(z.getNombreZona()));
            txtCoorLat.setText(val(z.getCoor_lat()));
            txtCoorLon.setText(val(z.getCoor_lon()));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error cargando zona: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
    
    private static String nvl(String s) { return (s == null ? "" : s); }
    private static String val(Double d) { return d == null ? "" : String.valueOf(d); }
    private static Double parseDoubleOrNull(String s) {
        if (s == null || s.isBlank()) return null;
        try { return Double.valueOf(s.replace(',', '.')); } catch (NumberFormatException e) { return null; }
    }
