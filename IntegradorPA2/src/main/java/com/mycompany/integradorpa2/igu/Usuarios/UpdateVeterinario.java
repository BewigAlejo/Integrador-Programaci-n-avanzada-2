    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        try {
            // base
            vet.setNombre(txtNombre.getText().trim());
            vet.setEmail(txtEmail.getText().trim());
            vet.setTelefono(txtTelefono.getText().trim());
            vet.setUsuario(txtUsuario.getText().trim());
            vet.setContrasenia(new String(txtContrasenia.getPassword()));

            // específicos
            vet.setEspecialidad(txtEspecialidad.getText().trim());
            vet.setMatricula(txtMatricula.getText().trim());

            dao.actualizar(vet);

            JOptionPane.showMessageDialog(this, "Veterinario actualizado correctamente.");
            dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se pudo actualizar: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_botonActualizarActionPerformed

