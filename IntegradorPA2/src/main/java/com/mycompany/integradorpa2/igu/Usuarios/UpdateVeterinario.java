     private void cargar() {
        try {
            Optional<Veterinario> opt = dao.buscarPorId(id);
            if (opt.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No se encontró el veterinario id=" + id,
                        "Aviso", JOptionPane.WARNING_MESSAGE);
                dispose();
                return;
            }
            vet = opt.get();

            // base
            txtNombre.setText(nvl(vet.getNombre()));
            txtEmail.setText(nvl(vet.getEmail()));
            txtTelefono.setText(nvl(vet.getTelefono()));
            txtUsuario.setText(nvl(vet.getUsuario()));
            txtContrasenia.setText(nvl(vet.getContrasenia()));

            // específicos
            txtEspecialidad.setText(nvl(vet.getEspecialidad()));
            txtMatricula.setText(nvl(vet.getMatricula()));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error cargando veterinario: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
            dispose();
        }
    }
     
    private static String nvl(String s) { return s == null ? "" : s; }
