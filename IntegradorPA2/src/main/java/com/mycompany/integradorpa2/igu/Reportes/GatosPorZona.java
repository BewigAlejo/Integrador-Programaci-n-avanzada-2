    private void configurarTabla() {
    modelo = new DefaultTableModel(
        new Object[]{"ID", "Nombre", "Raza", "Edad", "Estado", "Adoptado", "Zona"}, 0
    ) {
        @Override public boolean isCellEditable(int r, int c) { return false; }
        @Override public Class<?> getColumnClass(int c) {
            return switch (c) {
                case 0 -> Long.class;
                case 3 -> Integer.class;
                default -> String.class;
            };
        }
    };
    tablaGatos.setModel(modelo);
    tablaGatos.getTableHeader().setReorderingAllowed(false);
}

    
    private void cargarZonas() {
    zonasCargadas = zonaDao.listarTodos();          // trae desde BD

    javax.swing.DefaultComboBoxModel<String> model =
            new javax.swing.DefaultComboBoxModel<>();

    model.addElement("Todas");                      // índice 0 = sin filtro

    for (Zona z : zonasCargadas) {
        model.addElement(z.getNombreZona());        // solo mostramos el nombre
    }

    comboZonas.setModel(model);
}

    
    private void configurarFiltro() {
    sorter = new TableRowSorter<>(modelo);
    tablaGatos.setRowSorter(sorter);

    txtBuscar.getDocument().addDocumentListener(new javax.swing.event.DocumentListener() {
        public void insertUpdate(javax.swing.event.DocumentEvent e) { filtrarTexto(); }
        public void removeUpdate(javax.swing.event.DocumentEvent e) { filtrarTexto(); }
        public void changedUpdate(javax.swing.event.DocumentEvent e) { filtrarTexto(); }
    });
}

private void filtrarTexto() {
    String txt = txtBuscar.getText().trim();
    if (txt.isEmpty()) {
        sorter.setRowFilter(null);
    } else {
        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + txt));
    }
}


private void cargarTablaPorZona(Zona zonaSeleccionada) {
    modelo.setRowCount(0);

    java.util.List<Gato> gatos = gatoDao.listarTodos();

    for (Gato g : gatos) {
        Zona zg = g.getZona();

        // si hay filtro de zona y el gato no pertenece, saltar
        if (zonaSeleccionada != null) {
            if (zg == null || !zg.getId().equals(zonaSeleccionada.getId())) {
                continue;
            }
        }

        modelo.addRow(new Object[]{
            g.getId(),
            nvl(g.getNombre()),
            nvl(g.getRaza()),
            g.getEdad() != null ? g.getEdad() : 0,
            g.getEstadoDeSalud() != null ? g.getEstadoDeSalud().name() : "",
            Boolean.TRUE.equals(g.isAdoptado()) ? "Sí" : "No",
            (zg != null ? nvl(zg.getNombreZona()) : "")
        });
    }
}

private String nvl(String s) { return s == null ? "" : s; }


private void actualizarPorZona() {
    int idx = comboZonas.getSelectedIndex();

    Zona zonaSeleccionada = null;

    if (idx > 0) { // 0 = "Todas"
        zonaSeleccionada = zonasCargadas.get(idx - 1);
    }

    cargarTablaPorZona(zonaSeleccionada);
}


