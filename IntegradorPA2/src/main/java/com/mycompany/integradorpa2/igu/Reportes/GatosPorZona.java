    private void botonReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonReporteActionPerformed
        // TODO add your handling code here:
        int idx = comboZonas.getSelectedIndex();
    if (idx < 0) {
        JOptionPane.showMessageDialog(this, "Seleccioná una zona.");
        return;
    }

    String nombreZona;
    if (idx == 0) {
        nombreZona = "todas las zonas";
    } else {
        Zona z = zonasCargadas.get(idx - 1);
        nombreZona = z.getNombreZona();
    }

    int filas = modelo.getRowCount();  // cantidad de gatos actualmente visibles en la tabla

    JOptionPane.showMessageDialog(
        this,
        "En " + nombreZona + " hay " + filas + " gato(s).",
        "Reporte",
        JOptionPane.INFORMATION_MESSAGE
    );
    }//GEN-LAST:event_botonReporteActionPerformed

