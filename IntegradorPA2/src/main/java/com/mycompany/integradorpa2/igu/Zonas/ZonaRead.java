    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        // TODO add your handling code here:
        int viewRow = tablaZonas.getSelectedRow();
        if (viewRow < 0) {
            JOptionPane.showMessageDialog(this, "Seleccioná una zona de la tabla.");
            return;
        }
        int modelRow = tablaZonas.convertRowIndexToModel(viewRow);
        Long id = (Long) modelo.getValueAt(modelRow, 0);
        String nombre = String.valueOf(modelo.getValueAt(modelRow, 1));

        int r = JOptionPane.showConfirmDialog(
                this,
                "¿Eliminar la zona ID " + id + " (“" + nombre + "”)?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
        );
        if (r != JOptionPane.YES_OPTION) return;

        try {
            zonaDao.eliminar(id);
            JOptionPane.showMessageDialog(this, "Zona eliminada.");
            cargarTabla();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se pudo eliminar: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

