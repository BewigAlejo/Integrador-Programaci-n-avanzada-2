        private void configurarTabla() {
        modelo = new DefaultTableModel(
                new Object[]{"ID", "Nombre", "Coor_Lat", "Coor_Lon"}, 0) {
            @Override public boolean isCellEditable(int r, int c) { return false; }
            @Override public Class<?> getColumnClass(int columnIndex) {
                return switch (columnIndex) {
                    case 0 -> Long.class;      // ID
                    case 2, 3 -> Double.class; // lat/lon
                    default -> String.class;   // nombre
                };
            }
        };
        tablaZonas.setModel(modelo);
        tablaZonas.getTableHeader().setReorderingAllowed(false);
    }

    private void cargarTabla() {
        modelo.setRowCount(0);
        List<Zona> zonas = zonaDao.listarTodos();
        for (Zona z : zonas) {
            Long id = z.getId();
            String nombre = nvl(z.getNombreZona());
            Double lat = z.getCoor_lat();
            Double lon = z.getCoor_lon();
            modelo.addRow(new Object[]{ id, nombre, lat, lon });
        }
    }

    private static String nvl(String s) { return (s == null ? "" : s); }
    
    private void filtrar() {
        String t = txtBuscar.getText().trim();
        if (t.isEmpty()) sorter.setRowFilter(null);
        else sorter.setRowFilter(RowFilter.regexFilter("(?i)"+t));
    }
    
    private void abrirUpdateSeleccionado() {
        int viewRow = tablaZonas.getSelectedRow();
        if (viewRow < 0) return;
        int modelRow = tablaZonas.convertRowIndexToModel(viewRow);
        Long id = (Long) modelo.getValueAt(modelRow, 0);

        ZonaUpdate upd = new ZonaUpdate(id);
        upd.setLocationRelativeTo(this);
        upd.setVisible(true);
    }
