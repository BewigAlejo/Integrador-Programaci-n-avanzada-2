    private static Double parseDoubleOrNull(String s) {
        if (s == null || s.isBlank()) return null;
        try { return Double.valueOf(s.replace(',', '.')); } catch (NumberFormatException e) { return null; }
    }
    
    private void botonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCrearActionPerformed
        try {
            String nombre = txtNombre.getText().trim();
            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El nombre es obligatorio.");
                txtNombre.requestFocus();
                return;
            }

            Double lat = parseDoubleOrNull(txtCoorLat.getText().trim());
            Double lon = parseDoubleOrNull(txtCoorLon.getText().trim());

            Zona z = new Zona();
            z.setNombreZona(nombre);
            z.setCoor_lat(lat);
            z.setCoor_lon(lon);

            zonaDao.crear(z);

            JOptionPane.showMessageDialog(this, "Zona creada. ID = " + z.getId());
            // limpiar
            txtNombre.setText("");
            txtCoorLat.setText("");
            txtCoorLon.setText("");
            txtNombre.requestFocus();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se pudo crear la zona: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }//GEN-LAST:event_botonCrearActionPerformed

