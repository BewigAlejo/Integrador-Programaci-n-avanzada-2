    private void configurarTabla() {
        modelo = new DefaultTableModel(
                new Object[]{"ID", "Nombre", "Raza", "Edad", "Estado", "Adoptado"}, 0
        ) {
            @Override public boolean isCellEditable(int row, int column) { return false; }
            @Override public Class<?> getColumnClass(int col) {
                return switch (col) {
                    case 0 -> Long.class;
                    case 3 -> Integer.class;
                    default -> String.class;
                };
            }
        };
        tablaGatos.setModel(modelo);
        tablaGatos.getTableHeader().setReorderingAllowed(false);
    }

    private String nvl(String s) { return s == null ? "" : s; }

    private void cargarTabla() {
        modelo.setRowCount(0);
        List<Gato> gatos = gatoDao.listarTodos();   // o sólo disponibles, como prefieras
        for (Gato g : gatos) {
            Long id = g.getId();
            String nombre = nvl(g.getNombre());
            String raza   = nvl(g.getRaza());
            Integer edad  = g.getEdad() == null ? 0 : g.getEdad();
            String estado = g.getEstadoDeSalud() != null ? g.getEstadoDeSalud().name() : "";
            String adopt  = Boolean.TRUE.equals(g.isAdoptado()) ? "Sí" : "No";

            modelo.addRow(new Object[]{id, nombre, raza, edad, estado, adopt});
        }
    }

    private void configurarFiltro() {
        sorter = new TableRowSorter<>(modelo);
        tablaGatos.setRowSorter(sorter);

        DocumentListener dl = new DocumentListener() {
            public void insertUpdate(javax.swing.event.DocumentEvent e) { filtrar(); }
            public void removeUpdate(javax.swing.event.DocumentEvent e) { filtrar(); }
            public void changedUpdate(javax.swing.event.DocumentEvent e) { filtrar(); }
        };
        txtBuscar.getDocument().addDocumentListener(dl);
    }

    private void filtrar() {
        String texto = txtBuscar.getText().trim();
        if (texto.isEmpty()) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + texto));
        }
    }
    
    

