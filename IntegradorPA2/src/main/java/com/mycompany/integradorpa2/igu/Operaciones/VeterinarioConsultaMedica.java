    private void botonGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGenerarActionPerformed
        int viewRow = tablaGatos.getSelectedRow();
        if (viewRow < 0) {
            JOptionPane.showMessageDialog(this, "Seleccioná un gato de la tabla.");
            return;
        }

        int modelRow = tablaGatos.convertRowIndexToModel(viewRow);
        Long gatoId = (Long) modelo.getValueAt(modelRow, 0);

        // Abrir pantalla para registrar tratamiento / entrada
        new VeterinarioTratamiento(gatoId, veterinario).setVisible(true);
        dispose();
    }//GEN-LAST:event_botonGenerarActionPerformed

